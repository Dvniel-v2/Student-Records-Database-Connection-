    CONSTRAINT fk_project_member_student FOREIGN KEY (student_id)
        REFERENCES student(student_id) ON UPDATE CASCADE ON DELETE CASCADE,
    CONSTRAINT fk_project_member_staff FOREIGN KEY (staff_id)
