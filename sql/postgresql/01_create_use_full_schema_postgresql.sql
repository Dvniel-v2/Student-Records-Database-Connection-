CREATE OR REPLACE FUNCTION fn_enrolment_prereq_check()
RETURNS trigger
LANGUAGE plpgsql
AS $$
DECLARE
    missing_prereqs INTEGER := 0;
BEGIN
    IF TG_OP = 'INSERT' OR NEW.student_id <> OLD.student_id OR NEW.offering_id <> OLD.offering_id THEN
        SELECT COUNT(*) INTO missing_prereqs
        FROM course_prerequisite cp
        JOIN course_offering target_offering ON target_offering.course_id = cp.course_id
        JOIN academic_term target_term ON target_term.term_id = target_offering.term_id
        WHERE target_offering.offering_id = NEW.offering_id
          AND NOT EXISTS (
              SELECT 1
              FROM enrolment previous_enrolment
              JOIN course_offering previous_offering
                  ON previous_offering.offering_id = previous_enrolment.offering_id
              JOIN academic_term previous_term
                  ON previous_term.term_id = previous_offering.term_id
              WHERE previous_enrolment.student_id = NEW.student_id
                AND previous_offering.course_id = cp.prerequisite_course_id
                AND previous_enrolment.result_status = 'Passed'
                AND (
                    (cp.prerequisite_type = 'Prerequisite'
                        AND cp.can_be_concurrent = FALSE
                        AND previous_term.start_date < target_term.start_date)
                    OR
                    ((cp.prerequisite_type <> 'Prerequisite'
                        OR cp.can_be_concurrent = TRUE)
                        AND previous_term.start_date <= target_term.start_date)
                )
          );

        IF missing_prereqs > 0 THEN
            IF TG_OP = 'INSERT' THEN
                RAISE EXCEPTION 'Enrolment rejected: prerequisite or corequisite requirement is not satisfied.';
            ELSE
                RAISE EXCEPTION 'Update rejected: prerequisite or corequisite requirement is not satisfied.';
            END IF;
        END IF;
    END IF;
    RETURN NEW;
END;
$$;

CREATE TRIGGER trg_enrolment_prereq_before_insert
BEFORE INSERT ON enrolment
FOR EACH ROW EXECUTE FUNCTION fn_enrolment_prereq_check();

CREATE TRIGGER trg_enrolment_prereq_before_update
BEFORE UPDATE ON enrolment
FOR EACH ROW EXECUTE FUNCTION fn_enrolment_prereq_check();

