    CONSTRAINT uq_lecturer_expertise UNIQUE (lecturer_id, expertise_area),
    CONSTRAINT fk_expertise_lecturer FOREIGN KEY (lecturer_id)
        REFERENCES lecturer(lecturer_id) ON UPDATE CASCADE ON DELETE CASCADE
