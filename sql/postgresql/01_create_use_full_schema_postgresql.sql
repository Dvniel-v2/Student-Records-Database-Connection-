-- USE Full University Record Management System
-- PostgreSQL conversion of 01_create_use_full_database_v6_clean_roles.sql
-- This script creates a PostgreSQL schema named use_record_management inside the current database.

DROP SCHEMA IF EXISTS use_record_management CASCADE;
CREATE SCHEMA use_record_management;
SET search_path TO use_record_management, public;
CREATE EXTENSION IF NOT EXISTS pgcrypto;

-- =========================================================
-- 1. Organisation and academic structure
-- =========================================================

CREATE TABLE college (
    college_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    college_code VARCHAR(10) NOT NULL UNIQUE,
    college_name VARCHAR(120) NOT NULL UNIQUE,
    has_direct_students BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE department (
    department_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    college_id INT NOT NULL,
    department_code VARCHAR(10) NOT NULL UNIQUE,
    department_name VARCHAR(150) NOT NULL,
    research_area_summary VARCHAR(255) NULL,
    is_teaching_department BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT fk_department_college FOREIGN KEY (college_id)
        REFERENCES college(college_id) ON UPDATE CASCADE ON DELETE RESTRICT
);

CREATE TABLE administrative_unit (
    admin_unit_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    unit_code VARCHAR(10) NOT NULL UNIQUE,
    unit_name VARCHAR(120) NOT NULL UNIQUE,
    unit_description VARCHAR(255) NULL
);

-- =========================================================
-- 2. Programme and curriculum tables
-- =========================================================

CREATE TABLE programme (
    programme_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    programme_code VARCHAR(20) NOT NULL UNIQUE,
    programme_name VARCHAR(180) NOT NULL,
    degree_awarded VARCHAR(40) CHECK (degree_awarded IN ('B.Sc.', 'M.Sc.', 'Ph.D.')) NOT NULL,
    college_id INT NOT NULL,
    department_id INT NOT NULL,
    duration_years_min SMALLINT NOT NULL,
    duration_years_max SMALLINT NOT NULL,
    required_credit_hours SMALLINT NOT NULL,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT chk_programme_duration CHECK (duration_years_min <= duration_years_max),
    CONSTRAINT chk_programme_credits CHECK (required_credit_hours > 0),
    CONSTRAINT fk_programme_college FOREIGN KEY (college_id)
        REFERENCES college(college_id) ON UPDATE CASCADE ON DELETE RESTRICT,
    CONSTRAINT fk_programme_department FOREIGN KEY (department_id)
        REFERENCES department(department_id) ON UPDATE CASCADE ON DELETE RESTRICT
);

-- =========================================================
-- 3. People and role tables
-- =========================================================

CREATE TABLE person (
    person_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    first_name VARCHAR(60) NOT NULL,
    last_name VARCHAR(60) NOT NULL,
    email VARCHAR(150) NOT NULL UNIQUE,
    phone VARCHAR(40) NULL,
    nationality VARCHAR(80) NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE person_contact (
    contact_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    person_id INT NOT NULL,
    contact_type VARCHAR(40) CHECK (contact_type IN ('Email', 'Mobile', 'Address', 'Emergency', 'Other')) NOT NULL,
    contact_value VARCHAR(255) NOT NULL,
    is_primary BOOLEAN NOT NULL DEFAULT FALSE,
    CONSTRAINT fk_person_contact_person FOREIGN KEY (person_id)
        REFERENCES person(person_id) ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE student (
    student_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    person_id INT NOT NULL UNIQUE,
    student_number VARCHAR(30) NOT NULL UNIQUE,
    programme_id INT NOT NULL,
    date_of_birth DATE NOT NULL,
    enrolment_year SMALLINT NOT NULL,
    year_of_study SMALLINT NOT NULL,
    admission_type VARCHAR(40) CHECK (admission_type IN ('Local', 'International', 'Exchange')) NOT NULL DEFAULT 'International',
    student_status VARCHAR(40) CHECK (student_status IN ('Active', 'Suspended', 'Withdrawn', 'Graduated')) NOT NULL DEFAULT 'Active',
    graduation_status VARCHAR(40) CHECK (graduation_status IN ('Not eligible', 'Eligible', 'Graduated')) NOT NULL DEFAULT 'Not eligible',
    CONSTRAINT chk_student_year_of_study CHECK (year_of_study BETWEEN 1 AND 8),
    CONSTRAINT fk_student_person FOREIGN KEY (person_id)
        REFERENCES person(person_id) ON UPDATE CASCADE ON DELETE RESTRICT,
    CONSTRAINT fk_student_programme FOREIGN KEY (programme_id)
        REFERENCES programme(programme_id) ON UPDATE CASCADE ON DELETE RESTRICT
);

CREATE TABLE lecturer (
    lecturer_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    person_id INT NOT NULL UNIQUE,
    lecturer_number VARCHAR(35) NOT NULL UNIQUE,
    department_id INT NOT NULL,
    academic_rank VARCHAR(40) CHECK (academic_rank IN ('Lecturer', 'Assistant Professor', 'Associate Professor', 'Professor')) NOT NULL,
    employment_type VARCHAR(40) CHECK (employment_type IN ('Full-time', 'Part-time', 'Visiting', 'Contract')) NOT NULL DEFAULT 'Full-time',
    office_location VARCHAR(80) NULL,
    joining_year SMALLINT NOT NULL,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT fk_lecturer_person FOREIGN KEY (person_id)
        REFERENCES person(person_id) ON UPDATE CASCADE ON DELETE RESTRICT,
    CONSTRAINT fk_lecturer_department FOREIGN KEY (department_id)
        REFERENCES department(department_id) ON UPDATE CASCADE ON DELETE RESTRICT
);

CREATE TABLE administrator (
    admin_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    person_id INT NOT NULL UNIQUE,
    admin_number VARCHAR(30) NOT NULL UNIQUE,
    admin_unit_id INT NOT NULL,
    college_id INT NULL,
    department_id INT NULL,
    job_title VARCHAR(120) NOT NULL,
    employment_type VARCHAR(40) CHECK (employment_type IN ('Full-time', 'Part-time', 'Contract')) NOT NULL DEFAULT 'Full-time',
    joining_year SMALLINT NOT NULL,
    CONSTRAINT fk_administrator_person FOREIGN KEY (person_id)
        REFERENCES person(person_id) ON UPDATE CASCADE ON DELETE RESTRICT,
    CONSTRAINT fk_administrator_unit FOREIGN KEY (admin_unit_id)
        REFERENCES administrative_unit(admin_unit_id) ON UPDATE CASCADE ON DELETE RESTRICT,
    CONSTRAINT fk_administrator_college FOREIGN KEY (college_id)
        REFERENCES college(college_id) ON UPDATE CASCADE ON DELETE SET NULL,
    CONSTRAINT fk_administrator_department FOREIGN KEY (department_id)
        REFERENCES department(department_id) ON UPDATE CASCADE ON DELETE SET NULL
);

CREATE TABLE non_academic_staff (
    staff_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    person_id INT NOT NULL UNIQUE,
    staff_number VARCHAR(30) NOT NULL UNIQUE,
    admin_unit_id INT NULL,
    department_id INT NULL,
    job_title VARCHAR(120) NOT NULL,
    employment_type VARCHAR(40) CHECK (employment_type IN ('Full-time', 'Part-time', 'Contract')) NOT NULL DEFAULT 'Full-time',
    contract_start_date DATE NOT NULL,
    contract_end_date DATE NULL,
    salary_amount DECIMAL(10,2) NOT NULL,
    salary_currency CHAR(3) NOT NULL DEFAULT 'USD',
    CONSTRAINT chk_staff_contract_dates CHECK (contract_end_date IS NULL OR contract_start_date < contract_end_date),
    CONSTRAINT fk_staff_person FOREIGN KEY (person_id)
        REFERENCES person(person_id) ON UPDATE CASCADE ON DELETE RESTRICT,
    CONSTRAINT fk_staff_unit FOREIGN KEY (admin_unit_id)
        REFERENCES administrative_unit(admin_unit_id) ON UPDATE CASCADE ON DELETE RESTRICT,
    CONSTRAINT fk_staff_department FOREIGN KEY (department_id)
        REFERENCES department(department_id) ON UPDATE CASCADE ON DELETE RESTRICT
);

CREATE TABLE staff_emergency_contact (
    emergency_contact_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    person_id INT NOT NULL,
    contact_name VARCHAR(120) NOT NULL,
    relationship VARCHAR(60) NOT NULL,
    phone VARCHAR(40) NOT NULL,
    email VARCHAR(150) NULL,
    CONSTRAINT fk_staff_emergency_person FOREIGN KEY (person_id)
        REFERENCES person(person_id) ON UPDATE CASCADE ON DELETE CASCADE
);

