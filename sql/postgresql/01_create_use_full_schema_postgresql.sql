    source_code VARCHAR(30) NOT NULL UNIQUE,
    source_name VARCHAR(180) NOT NULL,
    source_type VARCHAR(40) CHECK (source_type IN ('Government', 'Industry', 'University', 'International', 'Other')) NOT NULL
