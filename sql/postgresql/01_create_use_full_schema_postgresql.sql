    end_date DATE NULL,
    is_current BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT chk_advisor_dates CHECK (end_date IS NULL OR start_date < end_date),
    CONSTRAINT fk_advisor_student FOREIGN KEY (student_id)
        REFERENCES student(student_id) ON UPDATE CASCADE ON DELETE CASCADE,
    CONSTRAINT fk_advisor_lecturer FOREIGN KEY (lecturer_id)
        REFERENCES lecturer(lecturer_id) ON UPDATE CASCADE ON DELETE RESTRICT
);

-- =========================================================
-- 5. Course, term and enrolment
-- =========================================================

CREATE TABLE academic_term (
    term_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    academic_year VARCHAR(9) NOT NULL,
    term_name VARCHAR(40) CHECK (term_name IN ('Fall', 'Spring', 'Summer')) NOT NULL,
    term_sequence SMALLINT NOT NULL,
    start_date DATE NOT NULL,
    end_date DATE NOT NULL,
    duration_weeks SMALLINT NOT NULL,
    is_regular_term BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT uq_academic_year_term UNIQUE (academic_year, term_name),
    CONSTRAINT chk_term_dates CHECK (start_date < end_date),
    CONSTRAINT chk_term_sequence CHECK (term_sequence IN (1, 2, 3)),
    CONSTRAINT chk_term_duration CHECK (duration_weeks IN (8, 16))
);

CREATE TABLE course (
    course_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    course_code VARCHAR(20) NOT NULL UNIQUE,
    course_name VARCHAR(180) NOT NULL,
    description TEXT NULL,
    department_id INT NOT NULL,
    course_level SMALLINT NOT NULL,
    credit_hours SMALLINT NOT NULL,
    course_type VARCHAR(40) CHECK (course_type IN ('Lecture', 'Lab', 'Thesis', 'Dissertation', 'Capstone', 'Seminar')) NOT NULL DEFAULT 'Lecture',
    parent_course_id INT NULL,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT chk_course_level CHECK (course_level IN (100, 200, 300, 400, 500, 600, 700, 800, 900)),
    CONSTRAINT chk_course_credit_hours CHECK (credit_hours BETWEEN 1 AND 14),
    CONSTRAINT fk_course_department FOREIGN KEY (department_id)
        REFERENCES department(department_id) ON UPDATE CASCADE ON DELETE RESTRICT,
    CONSTRAINT fk_course_parent FOREIGN KEY (parent_course_id)
        REFERENCES course(course_id) ON UPDATE CASCADE ON DELETE SET NULL
);

CREATE TABLE course_material (
    material_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    course_id INT NOT NULL,
    material_title VARCHAR(180) NOT NULL,
    material_type VARCHAR(40) CHECK (material_type IN ('Textbook', 'Lab manual', 'Reading', 'Software', 'Website', 'Other')) NOT NULL,
    material_reference VARCHAR(255) NOT NULL,
    CONSTRAINT fk_course_material_course FOREIGN KEY (course_id)
        REFERENCES course(course_id) ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE course_prerequisite (
    course_id INT NOT NULL,
    prerequisite_course_id INT NOT NULL,
    prerequisite_type VARCHAR(40) CHECK (prerequisite_type IN ('Prerequisite', 'Corequisite', 'Recommended')) NOT NULL DEFAULT 'Prerequisite',
    minimum_grade VARCHAR(10) NOT NULL DEFAULT 'Pass',
    can_be_concurrent BOOLEAN NOT NULL DEFAULT FALSE,
    PRIMARY KEY (course_id, prerequisite_course_id),
    CONSTRAINT fk_course_prereq_course FOREIGN KEY (course_id)
        REFERENCES course(course_id) ON UPDATE CASCADE ON DELETE RESTRICT,
    CONSTRAINT fk_course_prereq_required FOREIGN KEY (prerequisite_course_id)
        REFERENCES course(course_id) ON UPDATE CASCADE ON DELETE RESTRICT
);

CREATE TABLE programme_course_requirement (
    requirement_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    programme_id INT NOT NULL,
    course_id INT NOT NULL,
    requirement_type VARCHAR(40) CHECK (requirement_type IN ('Foundation', 'Core', 'Lab', 'Research', 'Thesis', 'Dissertation', 'Capstone')) NOT NULL,
    recommended_year SMALLINT NOT NULL,
    recommended_term VARCHAR(40) CHECK (recommended_term IN ('Fall', 'Spring', 'Summer')) NULL,
    is_required_for_graduation BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT uq_programme_required_course UNIQUE (programme_id, course_id),
    CONSTRAINT chk_requirement_year CHECK (recommended_year BETWEEN 1 AND 8),
    CONSTRAINT fk_requirement_programme FOREIGN KEY (programme_id)
        REFERENCES programme(programme_id) ON UPDATE CASCADE ON DELETE CASCADE,
    CONSTRAINT fk_requirement_course FOREIGN KEY (course_id)
        REFERENCES course(course_id) ON UPDATE CASCADE ON DELETE RESTRICT
);

CREATE TABLE elective_group (
    elective_group_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    programme_id INT NOT NULL,
    group_name VARCHAR(150) NOT NULL,
    minimum_credit_hours SMALLINT NOT NULL,
    recommended_year SMALLINT NULL,
    notes VARCHAR(255) NULL,
    CONSTRAINT uq_programme_elective_group UNIQUE (programme_id, group_name),
    CONSTRAINT chk_elective_min_credits CHECK (minimum_credit_hours > 0),
    CONSTRAINT fk_elective_group_programme FOREIGN KEY (programme_id)
        REFERENCES programme(programme_id) ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE elective_group_course (
    elective_group_id INT NOT NULL,
    course_id INT NOT NULL,
    PRIMARY KEY (elective_group_id, course_id),
    CONSTRAINT fk_elective_course_group FOREIGN KEY (elective_group_id)
        REFERENCES elective_group(elective_group_id) ON UPDATE CASCADE ON DELETE CASCADE,
    CONSTRAINT fk_elective_course_course FOREIGN KEY (course_id)
        REFERENCES course(course_id) ON UPDATE CASCADE ON DELETE RESTRICT
);

CREATE TABLE course_offering (
    offering_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    course_id INT NOT NULL,
    term_id INT NOT NULL,
    admin_id INT NOT NULL,
    section_code VARCHAR(10) NOT NULL DEFAULT 'A',
    capacity SMALLINT NOT NULL DEFAULT 35,
    delivery_mode VARCHAR(40) CHECK (delivery_mode IN ('In person', 'Online', 'Hybrid', 'Lab')) NOT NULL DEFAULT 'In person',
    schedule_days VARCHAR(30) NULL,
    start_time TIME NULL,
    end_time TIME NULL,
    status VARCHAR(40) CHECK (status IN ('Offered', 'Closed', 'Cancelled')) NOT NULL DEFAULT 'Offered',
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uq_course_term_section UNIQUE (course_id, term_id, section_code),
    CONSTRAINT chk_offering_capacity CHECK (capacity > 0),
    CONSTRAINT chk_offering_time CHECK (start_time IS NULL OR end_time IS NULL OR start_time < end_time),
    CONSTRAINT fk_offering_course FOREIGN KEY (course_id)
        REFERENCES course(course_id) ON UPDATE CASCADE ON DELETE RESTRICT,
    CONSTRAINT fk_offering_term FOREIGN KEY (term_id)
        REFERENCES academic_term(term_id) ON UPDATE CASCADE ON DELETE RESTRICT,
    CONSTRAINT fk_offering_admin FOREIGN KEY (admin_id)
        REFERENCES administrator(admin_id) ON UPDATE CASCADE ON DELETE RESTRICT
);

CREATE TABLE offering_lecturer (
    offering_id INT NOT NULL,
    lecturer_id INT NOT NULL,
    teaching_role VARCHAR(40) CHECK (teaching_role IN ('Primary', 'Co-teacher', 'Lab instructor', 'Supervisor')) NOT NULL DEFAULT 'Primary',
    workload_credit_hours DECIMAL(4,1) NOT NULL DEFAULT 3.0,
    PRIMARY KEY (offering_id, lecturer_id),
    CONSTRAINT chk_workload_credit_hours CHECK (workload_credit_hours > 0),
    CONSTRAINT fk_offering_lecturer_offering FOREIGN KEY (offering_id)
        REFERENCES course_offering(offering_id) ON UPDATE CASCADE ON DELETE CASCADE,
    CONSTRAINT fk_offering_lecturer_lecturer FOREIGN KEY (lecturer_id)
        REFERENCES lecturer(lecturer_id) ON UPDATE CASCADE ON DELETE RESTRICT
);

CREATE TABLE enrolment (
    enrolment_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    student_id INT NOT NULL,
    offering_id INT NOT NULL,
    enrolment_date DATE NOT NULL,
    enrolment_status VARCHAR(40) CHECK (enrolment_status IN ('Enrolled', 'Withdrawn', 'Completed')) NOT NULL DEFAULT 'Enrolled',
    result_status VARCHAR(40) CHECK (result_status IN ('In progress', 'Passed', 'Failed')) NOT NULL DEFAULT 'In progress',
    result_recorded_at TIMESTAMP NULL,
    CONSTRAINT uq_student_offering UNIQUE (student_id, offering_id),
    CONSTRAINT fk_enrolment_student FOREIGN KEY (student_id)
        REFERENCES student(student_id) ON UPDATE CASCADE ON DELETE RESTRICT,
    CONSTRAINT fk_enrolment_offering FOREIGN KEY (offering_id)
        REFERENCES course_offering(offering_id) ON UPDATE CASCADE ON DELETE RESTRICT
);

CREATE TABLE grade (
    grade_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    enrolment_id INT NOT NULL,
    assessment_type VARCHAR(40) CHECK (assessment_type IN ('Midterm', 'Final', 'Coursework', 'Lab', 'Project', 'Overall')) NOT NULL,
    numeric_grade DECIMAL(5,2) NOT NULL,
    letter_grade VARCHAR(3) NULL,
    recorded_by_lecturer_id INT NOT NULL,
    recorded_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT chk_numeric_grade CHECK (numeric_grade BETWEEN 0 AND 100),
    CONSTRAINT uq_enrolment_assessment UNIQUE (enrolment_id, assessment_type),
    CONSTRAINT fk_grade_enrolment FOREIGN KEY (enrolment_id)
        REFERENCES enrolment(enrolment_id) ON UPDATE CASCADE ON DELETE CASCADE,
    CONSTRAINT fk_grade_lecturer FOREIGN KEY (recorded_by_lecturer_id)
        REFERENCES lecturer(lecturer_id) ON UPDATE CASCADE ON DELETE RESTRICT
);

