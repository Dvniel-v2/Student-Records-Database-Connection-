CROSS JOIN course_prerequisite cp
JOIN course c ON c.course_id = cp.course_id
JOIN course pc ON pc.course_id = cp.prerequisite_course_id;

CREATE OR REPLACE VIEW vw_research_project_summary AS
SELECT
    rp.project_code,
    rp.project_title,
    rg.group_name,
    CONCAT(pi_person.first_name, ' ', pi_person.last_name) AS principal_investigator,
    rp.project_status,
    COUNT(DISTINCT rpm.project_member_id) AS team_member_count,
    COUNT(DISTINCT pf.project_funding_id) AS funding_source_count,
    COUNT(DISTINCT pp.publication_id) AS publication_count,
    COUNT(DISTINCT po.outcome_id) AS outcome_count
FROM research_project rp
JOIN research_group rg ON rg.research_group_id = rp.research_group_id
JOIN lecturer pi_lecturer ON pi_lecturer.lecturer_id = rp.principal_investigator_id
JOIN person pi_person ON pi_person.person_id = pi_lecturer.person_id
LEFT JOIN research_project_member rpm ON rpm.project_id = rp.project_id
LEFT JOIN project_funding pf ON pf.project_id = rp.project_id
LEFT JOIN project_publication pp ON pp.project_id = rp.project_id
LEFT JOIN project_outcome po ON po.project_id = rp.project_id
GROUP BY
    rp.project_id,
    rp.project_code,
    rp.project_title,
    rg.group_name,
    pi_person.first_name,
    pi_person.last_name,
    rp.project_status;

CREATE OR REPLACE VIEW vw_publications_past_year AS
SELECT
    pub.publication_code,
    pub.title,
    pub.publication_type,
    pub.publication_year,
    l.lecturer_number,
    CONCAT(p.first_name, ' ', p.last_name) AS lecturer_name
FROM publication pub
JOIN lecturer_publication lp ON lp.publication_id = pub.publication_id
JOIN lecturer l ON l.lecturer_id = lp.lecturer_id
JOIN person p ON p.person_id = l.person_id
WHERE pub.publication_year >= CAST(EXTRACT(YEAR FROM CURRENT_DATE) AS SMALLINT) - 1;



