CREATE OR REPLACE PROCEDURE sp_enrol_student(
    p_student_id INTEGER,
    p_offering_id INTEGER
)
LANGUAGE plpgsql
AS $$
DECLARE
    v_exists INTEGER := 0;
    v_status VARCHAR(20);
    v_capacity INTEGER := 0;
    v_current_enrolments INTEGER := 0;
    v_student_status VARCHAR(20);
BEGIN
    SELECT COUNT(*), MAX(student_status)
    INTO v_exists, v_student_status
    FROM student
    WHERE student_id = p_student_id;

    IF v_exists = 0 THEN
        RAISE EXCEPTION 'Enrolment rejected: student does not exist.';
    END IF;

    IF v_student_status <> 'Active' THEN
        RAISE EXCEPTION 'Enrolment rejected: student is not active.';
    END IF;

    SELECT COUNT(*) INTO v_exists
    FROM course_offering
    WHERE offering_id = p_offering_id;

    IF v_exists = 0 THEN
        RAISE EXCEPTION 'Enrolment rejected: course offering does not exist.';
    END IF;

    SELECT status, capacity
    INTO v_status, v_capacity
    FROM course_offering
    WHERE offering_id = p_offering_id
    FOR UPDATE;

    IF v_status <> 'Offered' THEN
        RAISE EXCEPTION 'Enrolment rejected: course offering is not open.';
    END IF;

    SELECT COUNT(*) INTO v_current_enrolments
    FROM enrolment
    WHERE offering_id = p_offering_id
      AND enrolment_status <> 'Withdrawn';

    IF v_current_enrolments >= v_capacity THEN
        RAISE EXCEPTION 'Enrolment rejected: course offering capacity has been reached.';
    END IF;

    INSERT INTO enrolment (student_id, offering_id, enrolment_date, enrolment_status, result_status)
    VALUES (p_student_id, p_offering_id, CURRENT_DATE, 'Enrolled', 'In progress');

    INSERT INTO dba_audit_log (action_name, action_detail)
    VALUES ('STUDENT_ENROLMENT', CONCAT('student_id=', p_student_id, ', offering_id=', p_offering_id));
END;
$$;

CREATE OR REPLACE PROCEDURE sp_record_student_grade(
    p_lecturer_id INTEGER,
    p_enrolment_id INTEGER,
    p_assessment_type VARCHAR(20),
    p_numeric_grade DECIMAL(5,2)
)
LANGUAGE plpgsql
AS $$
DECLARE
    v_locked_enrolment_id INTEGER := NULL;
    v_letter_grade VARCHAR(3);
    v_result_status VARCHAR(20);
BEGIN
    IF p_numeric_grade < 0 OR p_numeric_grade > 100 THEN
        RAISE EXCEPTION 'Grade rejected: numeric grade must be between 0 and 100.';
    END IF;

    v_letter_grade := CASE
        WHEN p_numeric_grade >= 90 THEN 'A'
        WHEN p_numeric_grade >= 80 THEN 'B'
        WHEN p_numeric_grade >= 70 THEN 'C'
        WHEN p_numeric_grade >= 60 THEN 'D'
        ELSE 'F'
    END;

    v_result_status := CASE WHEN p_numeric_grade >= 60 THEN 'Passed' ELSE 'Failed' END;

    SELECT e.enrolment_id INTO v_locked_enrolment_id
    FROM enrolment e
    JOIN course_offering co ON co.offering_id = e.offering_id
    JOIN offering_lecturer ol ON ol.offering_id = co.offering_id
    WHERE e.enrolment_id = p_enrolment_id
      AND ol.lecturer_id = p_lecturer_id
    FOR UPDATE;

    IF v_locked_enrolment_id IS NULL THEN
        RAISE EXCEPTION 'Grade rejected: lecturer is not assigned to this course offering.';
    END IF;

    INSERT INTO grade (
        enrolment_id,
        assessment_type,
        numeric_grade,
        letter_grade,
        recorded_by_lecturer_id
    )
    VALUES (
        p_enrolment_id,
        p_assessment_type,
        p_numeric_grade,
        v_letter_grade,
        p_lecturer_id
    )
    ON CONFLICT (enrolment_id, assessment_type) DO UPDATE SET
        numeric_grade = EXCLUDED.numeric_grade,
        letter_grade = EXCLUDED.letter_grade,
        recorded_by_lecturer_id = EXCLUDED.recorded_by_lecturer_id,
        recorded_at = CURRENT_TIMESTAMP;

    IF p_assessment_type = 'Overall' THEN
        UPDATE enrolment
        SET result_status = v_result_status,
            enrolment_status = 'Completed',
            result_recorded_at = CURRENT_TIMESTAMP
        WHERE enrolment_id = p_enrolment_id;
    END IF;

    INSERT INTO dba_audit_log (action_name, action_detail)
    VALUES ('GRADE_RECORDED', CONCAT('lecturer_id=', p_lecturer_id, ', enrolment_id=', p_enrolment_id));
END;
$$;

